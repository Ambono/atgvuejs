//==================================================================================
//	Dependencies
//==================================================================================
import gulp from 'gulp';
import jsdoc from 'gulp-jsdoc3';

//==================================================================================
//
//	JS Docs Task
//
//==================================================================================

//==================================================================================
//	Paths
//==================================================================================
const scriptSrc = "./int-src/js/**/*.js"; // Source Of Scripts

//==================================================================================
//	Image Minification
//==================================================================================
gulp.task("jsdoc", function(cb) {

    // Import Config
    const config = require('../../../jsdoc.json');
    
    // Compile Scripts
    return gulp.src(['int-src/docs/jsdocs/introduction.md', scriptSrc], {read: false})
    
    // Run JSDocs
    .pipe(jsdoc(config, cb));

});