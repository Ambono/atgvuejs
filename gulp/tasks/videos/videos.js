//==================================================================================
//	Dependencies
//==================================================================================
import gulp from 'gulp';

//==================================================================================
//
//	Videos Task
//
//  This is used to copy video files
//  int-src/videos -> int-dist/assets/videos
//
//==================================================================================

//==================================================================================
//	Paths
//==================================================================================
const videoPath = "int-src/videos/**/*";
const videoDest = "int-dist/assets/videos";

//==================================================================================
//  Videos Task
//==================================================================================
gulp.task("videos", function() {
    // Move Videos
    return gulp.src(videoPath)
    .pipe(gulp.dest(videoDest))
    
});